package com.mittal.model;

import java.util.Date;

public class Stock {
	
	String suplierName;
	String suplierInvoicenuber;
	String productcode;
	String modelno;
	String serialno;
	String date;
	
	
	public String getSuplierName() {
		return suplierName;
	}
	public void setSuplierName(String suplierName) {
		this.suplierName = suplierName;
	}
	public String getSuplierInvoicenuber() {
		return suplierInvoicenuber;
	}
	public void setSuplierInvoicenuber(String suplierInvoicenuber) {
		this.suplierInvoicenuber = suplierInvoicenuber;
	}
	public String getProductcode() {
		return productcode;
	}
	public void setProductcode(String productcode) {
		this.productcode = productcode;
	}
	public String getModelno() {
		return modelno;
	}
	public void setModelno(String modelno) {
		this.modelno = modelno;
	}
	public String getSerialno() {
		return serialno;
	}
	public void setSerialno(String serialno) {
		this.serialno = serialno;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}

}
